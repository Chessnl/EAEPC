public class ExamEdge {
    public int examA, examB;
    public int weight;


    public ExamEdge(int examA, int examB) {
        this.examA = examA;
        this.examB = examB;
    }

    public ExamEdge(int examA, int examB, int weight) {
        this.examA = examA;
        this.examB = examB;
        this.weight = weight;
    }
}
